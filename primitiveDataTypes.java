// Java Primitive data types, declaration and range
// array and string are non primitive in a different chapter
// 8 primitive data types


public class primitiveDataTypes{
	
	
	
	public static void main (String[] args){
		
		primitiveDataTypes object1 = new primitiveDataTypes();
		
	// Integer types has default value 0
				
		byte byteNum = 100; 	// 1 byte size
		short shortNum = 200;	// 2 byte size
		int intNum = 300;		// 4 byte size
		long longNum = 400L;	// 8 byte size ; notice L at the end. 
		
	
	// Floating or decimal data types
		
		float floatNum = 1.0f ;	// 4 bytes, note f at the end
		double doubleNum = 2.0; // 8 bytes
		
	// boolean data types, either hold true or false
	
		boolean a = true;
		boolean b = false;
	
	
	// character data type

		char charData = 'a';	// char with ' ', string " "
		
	
	
	}

}